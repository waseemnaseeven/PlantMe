/**
 * Get recipes filtered by diet objective
 */
export function getRecipesByDietObjective(dietObjective: string): Recipe[] {
  return mockRecipes.filter(recipe => 
    recipe.dietObjectives.includes(dietObjective as any)
  );
}

/**
 * Get featured recipes (highest rated)
 */
export function getFeaturedRecipes(limit: number = 6): Recipe[] {
  return [...mockRecipes]
    .sort((a, b) => (b.rating || 0) - (a.rating || 0))
    .slice(0, limit);
}

/**
 * Get quick recipes (prep + cook time <= 30 minutes)
 */
export function getQuickRecipes(): Recipe[] {
  return mockRecipes.filter(recipe => 
    (recipe.prepTime + recipe.cookTime) <= 30
  );
}
